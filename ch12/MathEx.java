package ch12;

public class MathEx {
	public static void main(String[] args) {
		System.out.println(Math.ceil(10.1)); // 올림
		System.out.println(Math.round(10.5)); // 반올림
		System.out.println(Math.floor(10.9)); // 버림
		System.out.println(Math.pow(10,3)); // 거듭제곱 : Math.pow(값,지수)
		System.out.println(Math.pow(2, 3)); // 거듭제곱
		System.out.println(Math.sqrt(100)); // 제곱근
	}

}
