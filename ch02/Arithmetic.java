package ch02;

public class Arithmetic {
	public static void main(String[] args) {
		int a = 10;
		// 정수형 대입
		int b = 7;
		
		System.out.println(a + b); // 17
		System.out.println(a - b); // 3
		System.out.println(a * b); // 70
		System.out.println(a / b); // 몫 : 1
		System.out.println(a % b); // 나머지 : 3
	}

}