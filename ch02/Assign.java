package ch02;

public class Assign {
	public static void main(String[] args) {
		
		int a = 10, b = 20;
		a += 5; // a = a + 5
		System.out.println(a); // 15
		a -= 5; // a = a - 5
		System.out.println(a); // 10
		b *= 5; // b = b * 5
		System.out.println(b); // 100
		b /= 2; // b = b / 2
		System.out.println(b); // 50
	}

}