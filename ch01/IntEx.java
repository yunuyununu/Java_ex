// public 완전공개 , private 비공개
public class IntEx {
// 메서드,함수 
	public static void main(String[] args) {
	// static 정적 void-비어있음(처리만)
		int a = 100;
		// 정수형 변수이름
		// 숫자-정수,실수 / 문자 'a' / 문자열(여러글자) "hello" ''틀림 / 불린형-참,거짓
		System.out.println(a);
		// 첫글자 대문자=>클래스
		// . 멤버
		// print line 줄바꿈
		
		String str = "hello";
		System.out.println(str);
		
	}
}