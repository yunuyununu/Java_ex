package ch01;

public class Bool {
	public static void main(String[] args) {
		// boolean은 논리형 참,거짓
		boolean a = true; // true, false만 적을 수 있음
		System.out.println(a); // true
		System.out.println(!a); // false // !는 부정 (= not)
		System.out.println(10 > 5); // true
		System.out.println(10 < 5); // false
	}

}